// Listas ou Arrays

const lista = [1, 2, 3, 'Nome'];


console.log(lista[3]);

// Adicionar itens

// Na última posição
lista.push('Evandro');

console.log(lista);

// Substituindo um valor
lista[2] = 'Novo';

console.log(lista);

// Apagar o último

lista.pop();

console.log(lista);

// Apagar o primeiro

lista.shift();

console.log(lista);